# 转义序列
print("I am 6'2\" tall.")
print('I am 6\'2" tall.')

#转义，\t相当于Tab键，\n换行
tabby_cat = "\tI'm rabbed in."
persian_cat = "\I'm split\non a line."
backslash_cat = "I'm \\ a \\ cat."
fat_cat = """
\nI'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
